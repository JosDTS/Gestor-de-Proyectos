    private void txtDateCommentFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtDateCommentFocusGained
        if (txtDateComment.getText().equals("Fecha Comentario")) {
            txtDateComment.setText(null);
            txtDateComment.requestFocus();

            recoverPass.DeleteStylePlaceHolder(txtDateComment);
        }
    }//GEN-LAST:event_txtDateCommentFocusGained

    private void txtCommentFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtCommentFocusGained
         if (txtComment.getText().equals("Comentario")) {
            txtComment.setText(null);
            txtComment.requestFocus();

            recoverPass.DeleteStylePlaceHolder(txtComment);
        }
    }//GEN-LAST:event_txtCommentFocusGained

    private void txtDateCommentFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtDateCommentFocusLost
         if (txtDateComment.getText().length() == 0) {
            recoverPass.StylePlaceHolder(txtDateComment);
            txtDateComment.setText("Fecha Comentario");
        }
    }//GEN-LAST:event_txtDateCommentFocusLost

    private void txtCommentFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtCommentFocusLost
        if (txtComment.getText().length() == 0) {
            recoverPass.StylePlaceHolder(txtComment);
            txtComment.setText("Comentario");
        }
    }//GEN-LAST:event_txtCommentFocusLost

